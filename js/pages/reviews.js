if (rowAEl && rowBEl) {
  const half = Math.ceil(REVIEWS.length / 2);
  const rowA = REVIEWS.slice(0, half);
  const rowB = REVIEWS.slice(half);

  const esc = (s) => s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
  const card = (r, dup) =>
    `<figure class="l3-rev__card"${dup ? ' aria-hidden="true"' : ''}>` +
    `<p class="l3-rev__q">“${esc(r.q)}”</p>` +
    `<figcaption class="l3-rev__w"><span class="l3-rev__a">${esc(r.a)}</span>${esc(r.w)}</figcaption>` +
    `</figure>`;

  // 원본 + 복제(aria-hidden) 로 seamless 무한 루프
  const fill = (el, list) => {
    el.innerHTML = list.map((r) => card(r, false)).join('') + list.map((r) => card(r, true)).join('');
  };
  fill(rowAEl, rowA);
  fill(rowBEl, rowB);
}
